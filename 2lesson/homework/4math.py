import math

a = int(input("Введите первое число (a): "))
b = int(input("Введите второе число (b): "))

print(f"\nРезультаты математических операций:")
print(f"Сумма: {a} + {b} = {a + b}")
print(f"Разность: {a} - {b} = {a - b}")
print(f"Произведение: {a} × {b} = {a * b}")
print(f"Частное: {a} ÷ {b} = {a / b}")
print(f"Остаток от деления: {a} % {b} = {a % b}")
print(f"Десятичный логарифм {a} = {math.log10(a)}")
print(f"{a} в степени {b} = {a ** b}")
